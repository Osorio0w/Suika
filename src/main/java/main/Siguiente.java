package main;

// Clase que representa el siguiente objeto a ser generado
public class Siguiente {
    private String value;

     // Constructor por defecto
    Siguiente() {
    }

    // Método para obtener el valor del siguiente objeto
    public String getValue() {
        return value;
    }

    // Método para establecer el valor del siguiente objeto
    public void setValue(String value) {
        this.value = value;
    }
}